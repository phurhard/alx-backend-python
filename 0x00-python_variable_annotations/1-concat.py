"""Annotations in  python
"""

def concat(str1: str, str2: str) -> str:
    """Returns the concatenation of two strings
    
    Keyword arguments:
    str1 -- first string ,
    str2 -- second string
    Return: returns the concatenation of both str1 and str2
    """
    
    return str1 + str2
